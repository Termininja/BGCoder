function solve(args) {
    var result = '';
    for (var l = args[0].length - 1; l > -1; l--) {
        result += [
            ["", "0", "00", "000", "01", "1", "10", "100", "1000", "02"],
            ["", "2", "22", "222", "23", "3", "32", "322", "3222", "24"],
            ["", "4", "44", "444", "45", "5", "54", "544", "5444", "46"],
            ["", "6", "66", "666"]
        ][l][args[0].split('').reverse()[l]];
    }

    return result;
}